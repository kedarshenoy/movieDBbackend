exports.getpopular = (req, res) => {
    const popular = req.param('popular');
    switch (popular) {
        case "OnTV":
            res.send(Ontv)
            break;
        case "Streaming":
            res.send(Streaming)
            break;

        case "For Rent":
            res.send(Rent)
            break;
        case "In Theaters":
            res.send(Theater)
            break;
        default:
            res.send(Streaming)
            break;
    }

}